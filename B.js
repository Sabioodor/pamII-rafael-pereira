function atv2 (){
    var a = prompt ("Digite um número")

    if (a > 0){
        alert("O número é positivo")
    }

    else if (a == 0) {
        alert ("O número é neutro (0)")
    }

    else if (a < 0){
        alert ("O número é negativo")
    }

    else {
        alert("número não válido")
    }
}